import random

# Список загадок и ответов
riddles = [
    ("Что можно увидеть с закрытыми глазами?", "сон"),
    ("Зимой и летом одним цветом.", "ёлка"),
    ("Не лает, не кусает, в дом не пускает.", "замок"),
    ("Без рук, без ног, а рисовать умеет.", "мороз"),
    ("Сто одежек и все без застежек.", "капуста"),
    ("Висит груша – нельзя скушать.", "лампочка"),
    ("Летит — молчит, лежит — молчит, как умрёт — заревёт.", "снег"),
    ("Днём спит, ночью летает, прохожих пугает.", "сова"),
    ("Без языка, а правду скажет.", "зеркало"),
    ("Течёт — не вытечет, бежит — не выбежит.", "время")
]

def play_riddles_game():
    selected = random.sample(riddles, 3)
    score = 0

    print("Угадай загадки! Введи свой ответ:")

    for i, (riddle, answer) in enumerate(selected, 1):
        print(f"\nЗагадка {i}: {riddle}")
        user_answer = input("Твой ответ: ").strip().lower()
        if user_answer == answer:
            print("Правильно!")
            score += 1
        else:
            print(f"Неправильно! Правильный ответ: {answer}")

    print(f"\nИгра окончена! Ты отгадал {score} из 3 загадок.")

# Запуск игры
if __name__ == "__main__":
    play_riddles_game()

